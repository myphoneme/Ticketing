package com.phoneme.ticketing.ui.dashboard.model;

public class DashboardSubheading {
    public String subheading;
    public String subheading_value;

    public void setSubheading(String subheading){
        this.subheading=subheading;
    }

    public void setSubheading_value(String subheading_value){
        this.subheading_value=subheading_value;
    }

    public String getSubheading(){
        return this.subheading;
    }

    public String getSubheading_value(){
        return this.subheading_value;
    }

}
